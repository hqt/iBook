extension UIColor {
    
    static func mainColor() -> UIColor {
        return UIColor(red: 123/255, green: 31/255, blue: 162/255, alpha: 1.0)
    }
    
    static func navColor() -> UIColor {
        return UIColor(red: 106/255, green: 27/255, blue: 154/255, alpha: 1.0)
    }
    
}